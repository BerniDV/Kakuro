    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String files = jTextField1.getText();
        String columnes = jTextField2.getText();
        if(files.length() > 0 && columnes.length() > 0){
            //if(files >= '0' && files <= '9')
            if((Character.isDigit(files.charAt(0))) && (Character.isDigit(columnes.charAt(0)))){
                int f = Integer.parseInt(files);
                int c = Integer.parseInt(columnes);
                if(f > 1 && c > 1){
                    CP.crearPartidaManual(f, c);
                    jLabel24.setText("");
                }
                else{
                    jLabel24.setText("Aquesta mida és incorrecta!");
                }
            }
            else{
                jLabel24.setText("La mida només accepta enters!");
            }
        }
        else{
            jLabel24.setText("No has introduit les mides del tauler!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

