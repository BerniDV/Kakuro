    private void mouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mouseClicked
        // TODO add your handling code here:
        
        if(evt.getSource()==nova){
            p0.setVisible(false);
            p1.setVisible(true);
            p2.setVisible(false);
            p3.setVisible(false);
            p4.setVisible(false);    
            String[] s = { "1", "2", "3", "4", "5", "6" };
            jComboBox2.removeAllItems();
            jComboBox2.addItem(s[1]);
            jComboBox2.addItem(s[2]);
            jComboBox2.addItem(s[3]);
            jComboBox2.addItem(s[4]);
            jComboBox2.addItem(s[5]);
           jComboBox3.removeAllItems();           
            jComboBox3.addItem(s[1]);
            jComboBox3.addItem(s[2]);
            jComboBox3.addItem(s[3]);
            jComboBox3.addItem(s[4]);
            jComboBox3.addItem(s[5]);
            
            String[] taulers = CP.getNomTaulersRepositori();
            jComboBox1.removeAllItems();
            for (int i = 0; i<taulers.length; i++){
                jComboBox1.addItem(taulers[i]);
            }
        }
        
        if(evt.getSource()==existent){
            p0.setVisible(false);
            p1.setVisible(false);
            p2.setVisible(true);
            p3.setVisible(false);
            p4.setVisible(false);
            
            int files = 0;
            try {
                files = CP.getFilesTaulerPartida();
            } catch (IOException ex) {
                Logger.getLogger(Panels.class.getName()).log(Level.SEVERE, null, ex);
            }
            int columnes = 0;
            try {
                columnes = CP.getColumnesTaulerPartida();
            } catch (IOException ex) {
                Logger.getLogger(Panels.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            if (files == 1){
                String missatge =  "No tens cap tauler pendent...";
                jLabel20.setText(missatge);
                jLabel21.setText("");
            }
            else{
                String missatge =  "Tens un tauler de mida " + files + " x " + columnes + " pendent.";
                jLabel20.setText(missatge);
            }
        }
        if(evt.getSource()==ranking){
            p0.setVisible(false);
            p1.setVisible(false);
            p2.setVisible(false);
            p3.setVisible(true);
            p4.setVisible(false);
            
            try {
                Map<String,Integer> map=CP.getRanking();
                int elems = map.size();
                String[] global = new String[elems];
                jList1.removeAll();
                int i = 0;
                for (Map.Entry<String, Integer> entry : map.entrySet()) {
                    String key = entry.getKey();
                    Integer value = entry.getValue();
                    String puntuacio = key + " : " + value;
                    global[i] = puntuacio;
                    i++;
                }
                //jList1 = new JList(global.toArray());
                
                jList1.setModel(new javax.swing.AbstractListModel<String>() {
                String[] strings = global;
                public int getSize() { return strings.length; }
                public String getElementAt(int i) { return strings[i]; }
                });
                
            } catch (IOException ex) {
                Logger.getLogger(Panels.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        if(evt.getSource()==info){
            p0.setVisible(false);
            p1.setVisible(false);
            p2.setVisible(false);
            p3.setVisible(false);
            p4.setVisible(true);

            String username = CP.getNomUsuari();
            String password = CP.getContrasenya();
            String record = String.valueOf(CP.getRecord());
            jLabel10.setText(username);
            jLabel11.setText(password);
            jLabel12.setText(record);
        }
    }//GEN-LAST:event_mouseClicked

